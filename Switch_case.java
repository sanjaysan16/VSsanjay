package openpractice;

public class Switch_case {
	public static void main(String[]args) {
		int san=6;
		
		switch(san) {
		case 1:
		{
			System.out.println("san");
			break;
		}
		case 2:
		{
			System.out.println("J");
			break;
		}
		default :
			System.out.println("bow" +" "+ "bow");
			
		}
	}
}
	